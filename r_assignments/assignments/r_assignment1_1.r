print("Part 1: Arithmetic Operation")

num1 <- 24
num2 <- 30

# Addition
addition <- num1 + num2
paste("Addition: ", num1, " + ", num2, " = ", addition)

# Subtraction
subtraction <- num1 - num2
paste("Subtraction: ", num1, " - ", num2, " = ", subtraction)

# Multiplication
multiplication <- num1 * num2
paste("Multiplication: ", num1, " * ", num2, " = ", multiplication)

# Division
division <- num1 / num2
paste("Division: ", num1, " / ", num2, " = ", division)

# Modulus
modulus <- num1 %% num2
paste("Modulus: ", num1, " %% ", num2, " = ", modulus)

# Integer Division
int_division <- num1 %/% num2
paste("Integer Division: ", num1, " %/% ", num2, " = ", int_division)

# Exponent
exponent <- num1 ^ num2
paste("Exponent: ", num1, " ^ ", num2, " = ", exponent)

